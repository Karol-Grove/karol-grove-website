/**
 * Perform Translation
 */
function translatePage(lang) {
  // 1. Core UI Elements Translation via data-tr
  document.querySelectorAll('[data-tr]').forEach(el => {
    const key = el.getAttribute('data-tr');
    if (uiTranslations[key]) {
      // Save original English content if not saved
      if (!el.getAttribute('data-orig-html')) {
        el.setAttribute('data-orig-html', el.innerHTML);
      }
      
      if (lang === 'en') {
        el.innerHTML = el.getAttribute('data-orig-html');
      } else {
        el.innerHTML = uiTranslations[key][lang] || el.getAttribute('data-orig-html');
      }
    }
  });

  // 2. Product Search Placeholder
  const searchInput = document.getElementById('product-search');
  if (searchInput) {
    if (!searchInput.getAttribute('data-orig-placeholder')) {
      searchInput.setAttribute('data-orig-placeholder', searchInput.placeholder);
    }
    if (lang === 'en') {
      searchInput.placeholder = searchInput.getAttribute('data-orig-placeholder');
    } else {
      searchInput.placeholder = uiTranslations['search_placeholder'][lang] || searchInput.placeholder;
    }
  }

  // 3. Product Cards Translation
  const productCards = document.querySelectorAll('.product-card');
  productCards.forEach(card => {
    const titleEl = card.querySelector('h3');
    if (!titleEl) return;

    const englishName = titleEl.getAttribute('data-orig-name') || titleEl.textContent.trim();
    if (!titleEl.getAttribute('data-orig-name')) {
      titleEl.setAttribute('data-orig-name', englishName);
    }

    const translation = productTranslations[englishName];
    if (translation) {
      // Translate Product Title
      if (lang === 'en') {
        titleEl.textContent = englishName;
      } else {
        titleEl.textContent = translation.title[lang] || englishName;
      }

      // Translate Benefits/Description
      const descEl = card.querySelector('p');
      if (descEl) {
        if (!descEl.getAttribute('data-orig-desc')) {
          descEl.setAttribute('data-orig-desc', descEl.textContent.trim());
        }
        if (lang === 'en') {
          descEl.textContent = descEl.getAttribute('data-orig-desc');
        } else {
          descEl.textContent = translation.desc[lang] || descEl.getAttribute('data-orig-desc');
        }
      }
    }

    // Translate Variant Label inside selector
    const variantSpan = card.querySelector('.variant-wrapper span');
    if (variantSpan) {
      if (!variantSpan.getAttribute('data-orig-text')) {
        variantSpan.setAttribute('data-orig-text', variantSpan.textContent.trim());
      }
      if (lang === 'en') {
        variantSpan.textContent = variantSpan.getAttribute('data-orig-text');
      } else {
        variantSpan.textContent = uiTranslations['variant_label'][lang] || variantSpan.getAttribute('data-orig-text');
      }
    }
  });
  
  // 4. Update Category heads h2 dynamically
  document.querySelectorAll('.product-category').forEach(cat => {
    const catId = cat.id;
    const key = 'cat_' + catId;
    const headH2 = cat.querySelector('.cat-head h2');
    if (headH2 && uiTranslations[key]) {
      if (!headH2.getAttribute('data-orig-html')) {
        headH2.setAttribute('data-orig-html', headH2.innerHTML);
      }
      if (lang === 'en') {
        headH2.innerHTML = headH2.getAttribute('data-orig-html');
      } else {
        // preserve emoji icon
        const emoji = headH2.innerHTML.split(' ')[0] || '';
        const isEmoji = emoji.charCodeAt(0) > 255 || headH2.innerHTML.includes('&#') || headH2.innerHTML.includes('&amp;#');
        const translatedText = uiTranslations[key][lang];
        if (isEmoji) {
          headH2.innerHTML = emoji + ' ' + translatedText;
        } else {
          headH2.innerHTML = translatedText;
        }
      }
    }
  });

  // 5. Update WhatsApp cart drawer titles & buttons dynamically (if rendered)
  updateDrawerTranslations(lang);
}

